function normalize(text: string) {
  return text.toLowerCase().trim();
}

export function filterMockEditorAnimes(query: string): EditorAnime[] {
  const q = normalize(query);
  if (q.length < 2) {
    return [];
  }
  return MOCK_EDITOR_ANIMES.filter(
    (a) =>
      normalize(a.title).includes(q) ||
      normalize(a.synopsis).includes(q) ||
      normalize(a.airedLabel).includes(q),
  ).slice(0, 15);
}
